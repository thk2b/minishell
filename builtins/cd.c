#include "builtin.h"
#include <unistd.h>

int	b_cd(char **av)
{
	if (av == NULL || av[0] == NULL)
		return (1);
	if (av[1] == NULL)
		return (chdir("~") == -1);
	return (chdir(av[1]) == -1);
}
